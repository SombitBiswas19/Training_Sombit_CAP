package operators;

public class Relational_operator {

	public static void main(String[] args) {
		//Return type is always boolean of this type of operator
		int a=6,b=5;
		System.out.println(a<b); // less than
		System.out.println(a>b); // greater than
		System.out.println(a<=b); // less than or equals to
		System.out.println(a>=b); // greater than or equals to
		System.out.println(a==b); //equals to
		System.out.println(a!=b); // not equals to

	}

}
