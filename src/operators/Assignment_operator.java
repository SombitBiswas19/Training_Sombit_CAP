package operators;

public class Assignment_operator {

	public static void main(String[] args) {
		int a=6; //Assignment Operator
		//Compound Assignment Operator
		//+=
		a+=3;
		System.out.println(a);
		//-=
	    a-=2;
	    System.out.println(a);
	    //*=
	    a*=8;
	    System.out.println(a);
	    // /=
	    a/=2;
	    System.out.println(a);
	    // %=
	    a%=5;
	    System.out.println(a);

	}

}
