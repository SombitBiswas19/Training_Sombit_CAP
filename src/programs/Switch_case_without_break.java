package programs;

public class Switch_case_without_break {

	public static void main(String[] args) {
		switch (2) {
		case 1: {
			System.out.println("c1");
		}
		case 2: {
			System.out.println("c2");
		}
		case 3: {
			System.out.println("c3");
		}
		default:
			System.out.println("No match");
		}

	}

}
