package programs;

public class Switch_Case_with_break {

	public static void main(String[] args) {
		switch (2) {
		case 1: {
			System.out.println("c1");
		}
		break;
		case 2: {
			System.out.println("c2");
		}
		break;
		case 3: {
			System.out.println("c3");
		}
		break;
		default:
			System.out.println("No match");
		
		}
	}

}
