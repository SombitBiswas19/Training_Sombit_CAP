package student_management_system;

public class Person {
	private String name;
	private int age;
	
	public String get_name()
	{
		return name;
	}
	public int get_age()
	{
		return age;
	}
	public void set_name(String name)
	{
		this.name=name;
	}
	public void set_age(int age)
	{
		this.age=age;
	}
	
}
