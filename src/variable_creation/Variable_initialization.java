package variable_creation;

public class Variable_initialization {
	public static void main(String[] args) {
		byte age=21; // 1 Byte
		short batch_size=1800; //2 Byte
		int select_size=215; //4 Byte
		long phone_no=8420077098l;//8 Byte
		String name="Sombit"; //Non-Primitive Datatype
		char gender='M';//2 Byte
		boolean marriage_status=false;//1 bit
		float weight=82.5f;//4 Byte
		double balance=10.00987654d;//8 Byte
		System.out.println(name);
		System.out.println(phone_no);
		System.out.println(gender);
		System.out.println(age);
		System.out.println(batch_size);
		System.out.println(select_size);
		System.out.println(marriage_status);
		System.out.println(weight);
		System.out.println(balance);
	}

}
